package org.whcraft.whcraft.client;

import net.fabricmc.fabric.api.datagen.v1.DataGeneratorEntrypoint;
import net.fabricmc.fabric.api.datagen.v1.FabricDataGenerator;
import org.whcraft.whcraft.client.datagen.BlockTagGenerator;
import org.whcraft.whcraft.client.datagen.ItemTagGenerator;
import org.whcraft.whcraft.client.datagen.WhcraftModelGenerator;

public class WhcraftDataGenerator implements DataGeneratorEntrypoint {

    @Override
    public void onInitializeDataGenerator(FabricDataGenerator fabricDataGenerator) {
        FabricDataGenerator.Pack pack = fabricDataGenerator.createPack();

        // 模型生成器
        pack.addProvider(WhcraftModelGenerator::new);

        // 标签生成器
        pack.addProvider(ItemTagGenerator::new);
        pack.addProvider(BlockTagGenerator::new);
    }
}